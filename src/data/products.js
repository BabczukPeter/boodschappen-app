// Helper function to get products by language
export const getProductsByLanguage = (language) => {
  return PRODUCTS.map(product => ({
    id: product.id,
    name: product[language] || product.en,
    category: product.category
  }));
};

// Helper function to search products
export const searchProducts = (query, language) => {
  if (!query || query.length < 1) return [];
  
  const lowerQuery = query.toLowerCase();
  const products = getProductsByLanguage(language);
  
  return products
    .filter(product => product.name.toLowerCase().includes(lowerQuery))
    .slice(0, 10); // Limit to 10 results
};
